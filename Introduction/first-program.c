#include <stdio.h>

int main() {
    printf("Hello, World!");
    return 0;
}

/*
Important things about printf:

- Everything you want to print should be kept inside parentheses ()
- The text to be printed is enclosed within double quotes ""
- Each printf statement ends with a semicolon ;

NB: Not following the above rules will result in errors and your code will not run successfully.
*/